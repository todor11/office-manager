package com.officemaneger.areas.paidHoursCalculator;

public class MonthlyPaidHoursEmployeeModel {

    private String month;

    private String paidHours;

    public MonthlyPaidHoursEmployeeModel() {
    }

    public String getMonth() {
        return this.month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getPaidHours() {
        return this.paidHours;
    }

    public void setPaidHours(String paidHours) {
        this.paidHours = paidHours;
    }
}
